        double n1 = (double) tblAluno.getValueAt(tblAluno.getSelectedRow(), 1);
        double n2 = (double) tblAluno.getValueAt(tblAluno.getSelectedRow(), 2);
        double n3 = (double) tblAluno.getValueAt(tblAluno.getSelectedRow(), 3);

        txfNome.setText(nome);
        txfNota1.setText(Double.toString(n1));
        txfNota2.setText(Double.toString(n2));
        txfNota3.setText(Double.toString(n3));

        btnSalvar.setText("Editar");

