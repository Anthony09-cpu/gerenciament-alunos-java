    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        DefaultTableModel tabela = (DefaultTableModel) tblAluno.getModel();
        if (tblAluno.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(rootPane, "Selecione uma linha!");
        } else {
            String nome = (String) tblAluno.getValueAt(tblAluno.getSelectedRow(), 0);
            if (JOptionPane.showConfirmDialog(rootPane, "Confirmar exclusão desse aluno: " + nome) == 0) {
                tabela.removeRow(tblAluno.getSelectedRow());
                txfNome.setText("");
                txfNota1.setText("");
                txfNota2.setText("");
                txfNota3.setText("");
                btnSalvar.setText("Editar");
                JOptionPane.showMessageDialog(rootPane, "Aluno Excluido!");
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed
    
